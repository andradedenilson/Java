package Teste.Java;

public class Operadores {
	public static void main (String[] args)
	 {
	/*Operadores ralacionais*/
		
		int valor6 = 90;
		int valor7 = 91;
		
		if (valor6 == valor7){
			System.out.println("Igual");
		}
		
		if (valor6 != valor7){
			System.out.println("Diferente");
		}
		
		if (valor6 < valor7){
			System.out.println("Menor");
		}

		if (valor6 > valor7){
			System.out.println("Maior");
		}
	 }
}
